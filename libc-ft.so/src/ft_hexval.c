#include <libft.h>

int		ft_hexval(char c)
{
	if (ft_isdigit(c))
	{
		return (c - 0060);
	}
	else if (c >= 0141 && c <= 0146)
	{
		return (c - 0141 + 10);
	}
	else if (c >= 0101 && c <= 0106)
	{
		return (c - 0101 + 10);
	}
	return (0);
}
