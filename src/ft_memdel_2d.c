#include <libft.h>

void				ft_memdel_2d(char **str)
{
	unsigned int	i;

	if (str)
	{
		i = 0;
		while (str[i])
		{
			ft_memdel((void *)&str[i]);
			i++;
		}
		free(str);
		str = NULL;
	}
}
