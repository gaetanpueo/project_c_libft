#include <libft.h>

int		ft_isxdigit(int c)
{
	if ((c >= 0060 && c <= 0071) ||
		(c >= 0101 && c <= 0106) ||
		(c >= 0141 && c <= 0146))
		return (1);
	return (0);
}
